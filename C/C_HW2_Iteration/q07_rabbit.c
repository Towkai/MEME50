#include<stdio.h>

/*
老王養了一群兔子，若三隻三隻一數，剩餘一隻；若五隻五隻一數，剩餘三隻；若七隻七隻一數，
剩餘二隻。試問兔子最少有幾隻。(58 隻)
*/

void main()
{
    printf("老王養了一群兔子，若三隻三隻一數，剩餘一隻；若五隻五隻一數，剩餘三隻；若七隻七隻一數，剩餘二隻。\n");
    int i = 0;
    while (1)
    {
        if ((i % 3 == 1) & (i % 5 == 3) & (i % 7 == 2))
            break;
        else
            i++;
    }
    printf("最少有 %d 隻\n", i);
}


